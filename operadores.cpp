#include <iostream>

int main(){

    int valorA = 2;
    int valorB = 2;

    std::cout << "La suma de A y B es: " << valorA + valorB << std::endl;
    std::cout << "La resta de A y B es: " << valorA - valorB << std::endl;
    std::cout << "La multiplicación de A y B es: " << valorA * valorB << std::endl;
    std::cout << "La división de A y B es: " << valorA / valorB << std::endl;
    std::cout << "El módulo de A y B es: " << valorA % valorB << std::endl;

    return 0;
}